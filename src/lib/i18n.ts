// ─── Dictionary registry ──────────────────────────────────────────────────

const dictionaries: Record<Lang, Dict> = { en, pl };

export const LANGS: Lang[] = ["en", "pl"];

export const LANG_LABELS: Record<Lang, string> = {
  en: "English",
  pl: "Polski",
};

export const LANG_COOKIE = "qlss-lang";
export const LANG_COOKIE_MAX_AGE = 60 * 60 * 24 * 365; // 1 year

// ─── Current language ─────────────────────────────────────────────────────
//
// On the **client**, `setLanguage()` is called by the Providers effect once
// the qlss-lang cookie is read, and the module-level `currentLang` is used.
//
// On the **server**, module-level state is shared across concurrent requests,
// so we use AsyncLocalStorage to scope the language per-request. Server
// components call `runWithLang(lang, () => ...)` (done automatically by the
// root layout) and `t()` reads from the ALS context when available.

let currentLang: Lang = "en";

// AsyncLocalStorage is only available on the server (Node). On the client it
// is a no-op wrapper.
let als: { run: (lang: Lang, cb: () => unknown) => unknown } | null = null;
if (typeof window === "undefined") {
  try {
    const { AsyncLocalStorage } = require("async_hooks");
    als = new AsyncLocalStorage<Lang>();
  } catch {
    als = null;
  }
}

/** Server-only: run a callback with the given language scoped to this request. */
export function runWithLang<R>(lang: Lang, cb: () => R): R {
  if (als) {
    return als.run(lang, cb) as R;
  }
  // Client fallback
  const prev = currentLang;
  currentLang = lang;
  try {
    return cb();
  } finally {
    currentLang = prev;
  }
}

export function setLanguage(lang: Lang): void {
  currentLang = lang;
  if (typeof document !== "undefined") {
    document.documentElement.lang = lang;
  }
}

export function getLanguage(): Lang {
  // Prefer the per-request ALS store on the server
  if (als && typeof window === "undefined") {
    try {
      const alsInstance = als as unknown as { getStore?: () => Lang | undefined };
      const storeLang = alsInstance.getStore?.();
      if (storeLang) return storeLang;
    } catch {
      // ignore
    }
  }
  return currentLang;
}

/** Read the language from the qlss-lang cookie (works on client only). */
export function readLangCookie(): Lang {
  if (typeof document === "undefined") return "en";
  const match = document.cookie.match(new RegExp(`(?:^|; )${LANG_COOKIE}=([^;]+)`));
  const val = match?.[1];
  if (val === "en" || val === "pl") return val;
  return "en";
}

/**
 * Resolve a Lang from either a raw cookie value ("pl") or a full cookie
 * header string ("qlss-lang=pl; other=..."). Falls back to "en".
 */
export function parseLangCookie(raw: string | undefined | null): Lang {
  if (!raw) return "en";
  // If it looks like a full cookie header, extract the qlss-lang value
  if (raw.includes("=") && raw.includes(LANG_COOKIE)) {
    const match = raw.match(new RegExp(`(?:^|; )${LANG_COOKIE}=([^;]+)`));
    const val = match?.[1]?.trim();
    if (val === "en" || val === "pl") return val;
    return "en";
  }
  // Otherwise treat it as a bare value
  const val = raw.trim();
  if (val === "en" || val === "pl") return val;
  return "en";
}

// ─── Helper: deep value access ────────────────────────────────────────────

function getNestedValue(obj: unknown, path: string[]): string {
  let current: unknown = obj;
  for (const key of path) {
    if (current === null || current === undefined || typeof current !== "object") {
      return path.join(".");
    }
    current = (current as Record<string, unknown>)[key];
  }
  return typeof current === "string" ? current : path.join(".");
}

/**
 * Look up a translation by dot-separated path segments.
 *
 * Falls back to English when a key is missing in the current language, then to
 * the dotted path when missing entirely.
 *
 * @example
 * t("common", "save")          // "save"
 * t("home.shorten_btn")        // "shorten"
 * t("legal", "privacy")        // "Privacy Policy"
 */
export function t(...path: string[]): string {
  const flatPath = path.flatMap((p) => p.split("."));
  const lang = getLanguage();
  const value = getNestedValue(dictionaries[lang], flatPath);
  if (value !== flatPath.join(".")) return value;
  // Fallback to English
  return getNestedValue(dictionaries.en, flatPath);
}
